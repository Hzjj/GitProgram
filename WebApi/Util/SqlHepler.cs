        /// <summary>
        /// 处理超过长度的字符串
        /// </summary>
        /// <param name="parms"></param>
        /// <returns></returns>
        /// 杨好兴
        /// 2008-05-22
        private static SqlParameter[] TrunParms(SqlParameter[] parms)
        {
            SqlParameter[] returnParms = new SqlParameter[parms.Length];
            for (int i = 0; i < parms.Length; i++)
            {
                returnParms[i] = parms[i];
                if (parms[i].Value != null && (parms[i].SqlDbType == SqlDbType.Char || parms[i].SqlDbType == SqlDbType.VarChar))
                {
                    byte[] strValue = System.Text.Encoding.Default.GetBytes(parms[i].Value.ToString());

                    if (strValue.Length > parms[i].Size)
                    {
                        parms[i].Value = System.Text.Encoding.Default.GetString(strValue, 0, parms[i].Size);
                    }
                }
            }
            return returnParms;
        }
